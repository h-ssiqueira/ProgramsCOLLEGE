TITLE ATIV7
.MODEL SMALL
.STACK 100h
.DATA
	M1	DB	"INSIRA UMA FRASE: $"
	FRA	DB	5 DUP(?)
	M2	DB	"FRASE INVERTIDA: $"
.CODE
	MOV AX,@DATA
	MOV DS,AX

	MOV BX,0
	MOV CX,30

	MOV AH,9
	LEA DX,M1
	INT 21h

INICIO:

	MOV AH,1
	INT 21h

	CMP AL,13
	JE INVERTE

	MOV FRA[BX],AL

	INC BX

	LOOP INICIO

INVERTE:

	MOV AH,9
	LEA DX,M2
	INT 21h

	MOV CX,BX

	DEC BX

FRASE:

	MOV AH,2
	MOV DL,FRA[BX]
	INT 21h

	DEC BX

	LOOP FRASE

	MOV AH,4Ch
	INT 21h

END