TITLE EX10
.MODEL SMALL
.STACK 100h
.DATA
	M1	DB	10,13,"INSIRA O CARACTER HEXADECIMAL: $"
	M2	DB	10,13,"ERRO! TENTE NOVAMENTE$"
	M3	DB	10,13,"NOVAMENTE?$"
	M4	DB	10,13,"VALOR EM DECIMAL: $"
.CODE
MOV AX,@DATA
MOV DS,AX

START:

MOV AH,9
LEA DX,M1
INT 21h

MOV AH,1
INT 21h

MOV BL,AL

CMP BL,48
JB ERRO

CMP BL,57
JBE NUM

CMP BL,65
JB ERRO

CMP BL,70
JBE LET

CMP BL,97
JB ERRO

CMP BL,102
JBE MLET

ERRO:

MOV AH,9
LEA DX,M2 
INT 21h

JMP START

MLET:

SUB BL,32

LET:

MOV AH,9
LEA DX,M4
INT 21h

MOV AH,2
MOV DL,49
INT 21h

SUB BL,17

MOV AH,2
MOV DL,BL
INT 21h

JMP AGAIN

NUM:

MOV AH,9
LEA DX,M4
INT 21h

MOV AH,2
MOV DL,BL
INT 21h

AGAIN:

MOV AH,9
LEA DX,M3
INT 21h

MOV AH,1
INT 21h

CMP AL,83
JE START

CMP AL,115
JE START

MOV AH,4Ch
INT 21h

END