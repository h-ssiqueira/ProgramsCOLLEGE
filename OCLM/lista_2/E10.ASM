TITLE E10
.MODEL SMALL
.STACK 100h
.CODE
LE_DEC PROC
XOR BX,BX

INICIO:

MOV AH,1
INT 21h

CMP AL,13
JE TERMINA
CMP AL,48
JB INICIO
CMP AL,57
JA INICIO

SUB AL,48 ; AND AL,0Fh

MOV CL,AL

MOV AX,10

MUL BX

XOR CH,CH

ADD AX,CX

MOV BX,AX
JMP INICIO

TERMINA:

RET
LE_DEC ENDP

MAIN PROC

CALL LE_DEC



MOV AH,4Ch
INT 21h
MAIN ENDP
END MAIN