TITLE ATIV5
.MODEL SMALL
.STACK 100h
.DATA
	MSG1	DB	10,13,"DIGITE O DIVIDENDO: $"
	MSG2	DB	10,13,"DIGITE O DIVISOR: $"
	MSG3	DB	10,13,"QUOCIENTE: $"
	MSG4	DB	10,13,"RESTO: $"
.CODE
MOV AX,@DATA
MOV DS,AX

; DIVIDENDO = RESTO = BL
; DIVISOR = BH
; QUOCIENTE = CL

MOV CL,48 ;CONTADOR DE DIVISÕES COMEÇA COM 0

MOV AH,9
LEA DX,MSG1
INT 21h

MOV AH,1
INT 21h

MOV BL,AL ; BL FICA COM O DIVIDENDO
SUB BL,48 ; SUBTRAI 48 DO DIVIDENDO

MOV AH,9
LEA DX,MSG2
INT 21h

MOV AH,1
INT 21h

MOV BH,AL ; BH FICA COM O DIVISOR
SUB BH,48 ; SUBTRAI 48 DO DIVISOR

; COMPARAÇÃO SE OS DOIS FOREM 0
CMP BH,0 
JE FIM
CMP BL,0
JE FIM

; COMPARAÇÃO SE DIVISOR É MAIOR QUE DIVIDENDO
CMP BL,BH
JB FIM

INICIO:
SUB BL,BH ; SUBTRAI O DIVISOR DO DIVIDENDO
INC CL ; INCREMENTA 1 AO QUOCIENTE
CMP BL,BH ; COMPARA O DIVISOR COM O DIVIDENDO
JB FIM ; SE DIVISOR FOR MENOR QUE DIVIDENDO PULA PARA O FIM

JMP INICIO ; SENÃO ELE VOLTA PARA O INICIO

FIM:

MOV AH,9
LEA DX,MSG3
INT 21h

MOV AH,2
MOV DL,CL
INT 21h

MOV AH,9
LEA DX,MSG4
INT 21h

ADD BL,48 ; INCREMENTA 48 AO RESTO

MOV AH,2
MOV DL,BL
INT 21h

MOV AH,4Ch
INT 21h

END