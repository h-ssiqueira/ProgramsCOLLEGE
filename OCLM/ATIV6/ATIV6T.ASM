TITLE ATIV6A
.MODEL SMALL
.STACK 100h
.DATA
	MSG		DB	"INSIRA UM CARACTER: $"
	MSG1	DB	10,13,"EM BINARIO: $"
.CODE
MOV AX,@DATA
MOV DS,AX

MOV CL,8

MOV AH,9
LEA DX,MSG
INT 21h

MOV AH,1
INT 21h

MOV BL,AL

MOV AH,9
LEA DX,MSG1
INT 21h

TESTE:
SHL BL,1
JC UM

ZERO:
MOV AH,2
MOV DL,48
INT 21h

DEC CL
JZ FIM
JNZ TESTE

UM:
MOV AH,2
MOV DL,49
INT 21h

DEC CL
JNZ TESTE

FIM:

MOV AH,4Ch
INT 21h
END
