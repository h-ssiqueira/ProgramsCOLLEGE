TITLE ATIV5
.MODEL SMALL
.STACK 100h
.DATA
	MSG		DB	"INSIRA UM CARACTER: $"
	MSG1	DB	10,13,"EM BINARIO: $"
.CODE
MOV AX,@DATA
MOV DS,AX

MOV CL,8

MOV AH,9
LEA DX,MSG
INT 21h

MOV AH,1
INT 21h

MOV BL,AL

MOV AH,9
LEA DX,MSG1
INT 21h

MOV BH,10000000b

INICIO:
	CMP CL,0
	JE FIM
	
	TEST BL,BH
	JNZ UM
	
ZERO:
MOV AH,2
MOV DL,48
INT 21h

SHR BH,1
DEC CL
JMP INICIO

UM:
MOV AH,2
MOV DL,49
INT 21h

SHR BH,1
DEC CL
JMP INICIO

FIM:

MOV AH,4Ch 
INT 21h
END