TITLE ATIV8
.MODEL SMALL
.STACK 100h
.DATA
M1	DB	"DIGITE UMA FRASE: $"
M2	DB	10,13,"FRASE INVERTIDA: $"
.CODE

INVERTE PROC

MOV AH,9
LEA DX,M1
INT 21h

LER:
MOV AH,1
INT 21h

CMP AL,13
JE COUNT

PUSH AX

INC BH

LOOP LER

COUNT:
MOV AH,9
LEA DX,M2
INT 21h

EXIBIR:

DEC BH

MOV AH,2
POP DX
INT 21h

;DEC BH

CMP BH,0
JNZ EXIBIR

RET

INVERTE ENDP

MAIN PROC

MOV AX,@DATA
MOV DS,AX

MOV BH,0
MOV CX,30

CALL INVERTE

MOV AH,4Ch
INT 21h

MAIN ENDP
END MAIN