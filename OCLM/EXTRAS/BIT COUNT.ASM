TITLE BITCOUNT
.MODEL SMALL
.STACK 100h
.DATA
M1	DB	"CARACTER: $"
M2	DB	10,13,"BITS 1: $"
.CODE
MOV AX,@DATA
MOV DS,AX

XOR BX,BX
MOV AH,9
LEA DX,M1
INT 21h

MOV AH,1
INT 21h

MOV CX,8
VERIF:
SHR AL,1
JNC CONT
INC BL
CONT:
LOOP VERIF
MOV AH,9
LEA DX,M2
INT 21h
MOV AH,2
ADD BL,48
MOV DL,BL
INT 21h
MOV AH,4Ch
INT 21h
END