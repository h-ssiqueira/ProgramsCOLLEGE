TITLE ativ2
.MODEL SMALL
.STACK 100h
.DATA
	M1 DB "Digite o primeiro numero: $"
	M2 DB "Digite o segundo numero: $"
	M3 DB "Soma dos numeros: $"
	
.CODE
	MOV AX,@DATA ; PADRAO DE STRING
	MOV DS,AX

	MOV AH,9 ; PRINT STRING1
	LEA DX,M1
	INT 21h

	MOV AH,1 ; COLETAR PRIMEIRO NUMERO
	INT 21h
	
	MOV BL,AL ; ARMAZENAR EM BL
	
	MOV AH,2 ; PULA LINHA
	MOV DL,10
	INT 21h
	
	MOV AH,9 ; PRINTF STRING2
	LEA DX,M2
	INT 21h
	
	MOV AH,1 ; COLETAR SEGUNDO NUMERO
	INT 21h
	
	ADD AL,BL ; SOMA
	SUB AL,48
	
	MOV CL,AL ; MOVER PARA CL
	
	MOV AH,2 ; PULA LINHA
	MOV DL,10
	INT 21h
	
	MOV AH,9 ; PRINT STRING3
	LEA DX,M3
	INT 21h
	
	MOV AH,2 ; PRINT SOMA
	MOV DL,CL
	INT 21h
	
	MOV AH,4CH ; ENCERRAR PROGRAMA
	INT 21h
END