TITLE EX11
.MODEL SMALL
.STACK 100h
.DATA
	M1	DB	"PRIMEIRO NUMERO: $"
	M2	DB	10,13,"SEGUNDO NUMERO: $"
	M3 	DB	10,13,"RESULTADO: $"
.CODE
MOV AX,@DATA
MOV DS,AX

MOV CL,48

MOV AH,9
LEA DX,M1
INT 21h

MOV AH,1
INT 21h

MOV BL,AL

MOV AH,9
LEA DX,M2
INT 21h

MOV AH,1
INT 21h

MOV BH,AL

SUB BL,48
JZ RESULT

SUB BH,48
JZ RESULT

MULT:

ADD CL,BL

DEC BH
JZ RESULT
JNZ MULT

RESULT:

MOV AH,9
LEA DX,M3
INT 21h

MOV AH,2
MOV DL,CL
INT 21h

MOV AH,4Ch
INT 21h
END