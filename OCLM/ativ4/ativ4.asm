TITLE ATIVIDADE4
.MODEL SMALL
.STACK 100h
.CODE

MOV CX,20 ; CONDICIONAL PARA O LOOP LETRAS
MOV BL,97 ; CARACTER DE INICIO DO LOOP
MOV BH,4 ; CONTADOR PARA PULAR LINHA
JMP LETRAS

PULA:

MOV AH,2
MOV DL,10
INT 21h

MOV AH,2
MOV DL,13
INT 21h

MOV BH,4

LETRAS:

MOV AH,2
MOV DL,BL ; PRINT LETRA
INT 21h

INC BL ; INCREMENTA NAS LETRAS
DEC BH ; DECREMENTA O CONTADOR DE PULO DE LINHA
JZ PULA ; SE = 0 PULA LINHA

LOOP LETRAS

MOV AH,4Ch
INT 21h
END