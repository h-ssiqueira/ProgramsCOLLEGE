TITLE ATIV3
.MODEL SMALL
.STACK 100h
.DATA
	INP DB "Digite um caracter: $"
	NUM DB 10,13,"O carater digitado eh um numero.$"
	LET DB 10,13,"O caracter digitado eh uma letra.$"
	SIM DB 10,13,"O carater digitado eh um simbolo.$"
.CODE
MOV AX,@DATA
MOV DS,AX

MOV AH,9
LEA DX,INP ; IMPRIMIR MENSAGEM
INT 21h

MOV AH,1 ; INPUT
INT 21h

MOV BL,AL ; MOVER PARA OUTRO REGISTRADOR

CMP BL,48 ; COMPARAR COM 0
JB SIMBOLO

CMP BL,57 ; COMPARAR COM 9
JBE NUMERO

CMP BL,65 ; COMPARAR COM A
JB SIMBOLO

CMP BL,90 ; COMPARAR COM Z
JBE LETRA

CMP BL,97 ; COMPARAR COM a
JB SIMBOLO

CMP BL,122 ; COMPARAR COM z
JBE LETRA
JA SIMBOLO


NUMERO:
MOV AH,9
LEA DX,NUM
INT 21h
JMP FIM

LETRA:
MOV AH,9
LEA DX,LET
INT 21h
JMP FIM

SIMBOLO:
MOV AH,9
LEA DX,SIM
INT 21h
JMP FIM

FIM:
MOV AH,4Ch
INT 21h

END