TITLE EX5
.MODEL SMALL
.STACK 100h
.DATA
	M1	DB	10,13,"LETRA MAIUSCULA.$"
	M2	DB	10,13,"LETRA MINUSCULA.$"
	M3	DB	10,13,"NAO E LETRA.$"
.CODE
	MOV AX,@DATA
	MOV DS,AX

	MOV AH,1
	INT 21h

	MOV BL,AL

	CMP BL,65
	JB NLET

	CMP BL,90
	JBE LETMA

	CMP BL,97
	JB NLET

	CMP BL,122
	JBE LETMI
	JA NLET

NLET:
	MOV AH,9
	LEA DX,M3
	INT 21h

	JMP FIM

LETMA:
	MOV AH,9
	LEA DX,M1
	INT 21h

	JMP FIM

LETMI:
	MOV AH,9
	LEA DX,M2
	INT 21h

FIM:
	MOV AH,4Ch
	INT 21h

END