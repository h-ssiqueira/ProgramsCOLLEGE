TITLE INITIALS
.MODEL SMALL
.STACK 100h
.DATA
    M1		DB	"NAME: $"
    M2		DB	10,13,"INICIALS: $"
NOME	DB	100 DUP(?)
.CODE
    MOV AX,@DATA
    MOV DS,AX

    MOV AH,9
    LEA DX,M1
    INT 21H

    XOR SI,SI
LETS:
    MOV AH,1
    INT 21h

    CMP AL,13
    JE EXIB

    MOV NOME[SI],AL
    INC SI

    LOOP LETS

EXIB:
    MOV AH,9
    LEA DX,M2
    INT 21h

    MOV CX,SI
    XOR SI,SI

    MOV AH,2
    MOV DL,NOME[0]
    INT 21h

SHOW:
    CMP NOME[SI],32
    JE ADI

    INC SI
    JMP REPET

ADI:
    INC SI

    MOV AH,2
    MOV DL,NOME[SI]
    INT 21h

REPET:
    LOOP SHOW

    MOV AH,4Ch
    INT 21h
END